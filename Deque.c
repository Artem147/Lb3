#define _CRT_SECURE_NO_WARNINGS 
#include <stdio.h>
#include <malloc.h>
#include <stdlib.h>
#include "Deque.h"

//Инициализация дека. 
int Initialize(Deque* D, int number)
{
	D->size = 2 * number;
	D->data = (int*)malloc(D->size*sizeof(int));
	if (!D->data) return 1;
	if (number == 1)
		D->start = 1;
	else D->start = number;
	D->final = D->start-1;
	return 0;
};

//Положить x в начало. 1, если дек переполнен.
int PushFront(Deque* D, int x)
{
	if (IsFull == 1) return 1;
	D->start--;
	 D->data[D->start]=x;
	return 0;
}

//Положить x в конец. 1, если дек переполнен.
int PushBack(Deque* D, int x)
{
	if (IsFull == 1) return 1;
	D->final++;
	D->data[D->final] = x;
	return 0;
}

//Взять из начала. 
int PopFront(Deque* D)
{
	D->start++;
	return D->data[D->start - 1];
}

//Взять из конца.
int PopBack(Deque* D)
{
	D->final--;
	return D->data[D->final + 1];
}

//проверка пустой дек или нет. 1 если пустой, 0 если непустой.
int IsEmpty(Deque* D)
{
	if (D->start > D->final) return 1;
	else return 0;
}

//проверка полный дек или нет. 1 если полный, 0 если неполный.
int IsFull(Deque* D)
{
	if (D->start <=0 || D->final >= D->size) return 1;
	else return 0;
}

//Вывод дека на экран.
void PrintData(Deque* D)
{
	int i;
	if (IsEmpty == 1) return;
	//Если в деке один элемент.
	if (D->start == D->final) 
		printf(D->data[D->start]);
	//Если в деке не один элемент.
	else 
		for (i = D->start; i <= D->final; i++)
			printf("%d ", D->data[i]);
}